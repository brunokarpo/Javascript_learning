/* Arrays vs Objects */
var foxArray = ['Fox', 'Prata', 'VW']; // Criando array

console.log('typeof foxArray', typeof foxArray); // object
console.log('foxArray[0]', foxArray[0]); // Como acessar valor de um array
console.log('foxArray[1]', foxArray[1]);

var foxObj = {modelo: 'fox', cor: 'prata', fabricante: 'VW'}; // criando objeto

console.log('typeof foxObj', typeof foxObj); // object
console.log('foxObj.modelo', foxObj.modelo); // como acessa atr√≠buto de um objeto
console.log('foxObj.cor', foxObj.cor);

console.log('foxArray', foxArray);
console.log('foxObj', foxObj);